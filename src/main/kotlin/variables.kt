fun main(args: Array<String>) {
    //mutable variables-can change variable
    var name="eMobilis Mobile Technology"
    name="University of Nairobi"
    var jina="Esther"
    var nambari=18
    var nambari2=6

    println(name)
    println("My name is $jina")
    println("My name is "+jina)
    println(nambari)
    println("My name is "+jina +" and I am $nambari years")
    println("The product of $nambari and $nambari2 is "+nambari*nambari2)
    println("The quotient of $nambari and $nambari2 is "+nambari/nambari2)
    println("The difference of $nambari and $nambari2 is "+(nambari - nambari2) )
    println("The sum of $nambari and $nambari2 is "+(nambari + nambari2))


    //immutable variable-can't change a variable
    val me="Mahita"
    println(me)



}